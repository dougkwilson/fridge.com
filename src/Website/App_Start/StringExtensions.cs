        private static Regex _regex = CreateEmailValidationRegex();

        /// <summary>Determines whether the specified value matches the pattern of a valid email address.</summary>
        /// <returns>true if the specified value is valid, null or empty; otherwise, false.</returns>
        /// <param name="value">The value to validate.</param>
        public static Boolean IsEmail(this String value) {
            String email = value as String;
            if (String.IsNullOrWhiteSpace(email))
                return false;

            if (_regex != null && _regex.Match(email).Length > 0) {
                return true;
            }

            Int32 atCount = 0, dotCount = 0, index = email.Length - 1, atIndex = email.IndexOf('@');
            foreach (int c in email) {
                if (c == 64)
                    ++atCount;
                if (atCount > 1)
                    if (c == '.')
                        ++dotCount;
            }
            if (atCount != 1 || dotCount < 1 || email[0] == 64 || email[index] == 64)
                return false;

            if (atIndex > email.IndexOf('.', atIndex + 2))
                return false;

            return true;
        }

